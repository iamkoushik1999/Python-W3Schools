# append
myList1 = ['apple', 'banana']
myList1.append('kiwi')
print(myList1)
print('-------------------------------------')
# clear
myList1.clear()
print(myList1)
print('-------------------------------------')
# copy
myList2 = [1, 2, 3, 4]
myList3 = myList2.copy()
print(myList2)
print('-------------------------------------')
# count
myList4 = [99, 79, 69, 99, 33]
count = myList4.count(99)
print(count)
print('-------------------------------------')
# extend
myList5 = ['cricket', 'football']
myList6 = ['rugby', 'baseball']
myList5.extend(myList6)
print(myList5)
print('-------------------------------------')
# index
myList7 = ['kolkata', 'mumbai', 'delhi', 'punjab']
index = myList7.index('kolkata')
print(index)
print('-------------------------------------')
# insert
myList8 = ['apple', 'banana', 'kiwi']
