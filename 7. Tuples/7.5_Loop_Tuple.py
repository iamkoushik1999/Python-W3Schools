# For Loop
basket1 = ('apple', 'banana', 'cherry')

for x in basket1:
    print(x)
print('-----------------------------------------')
# For Loop with Range and Lenth
for i in range(len(basket1)):
    print(basket1[i])
print('-----------------------------------------')
# While loop
i = 0
while i < len(basket1):
    print(basket1[i])
    i = i + 1
