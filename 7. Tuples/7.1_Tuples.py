myTuple1 = ('apple', 'banana', 'kiwi')
print(myTuple1)
# Tuple items are ordered, unchangeable and allow duplicate values
# Length
print(len(myTuple1))
# One Tuple Item
myTuple2 = ('apple',)
print(myTuple2)
print(type(myTuple2))
# Not a Tuple
myTuple3 = ('apple')
print(myTuple3)
print(type(myTuple3))
# Tuple Can Contain any data type
anyTuple = ('apple', 1, True, False)
print(anyTuple)
# Tuple Constructor
# Use tuple() constructor to make a tuple
thisTuple = tuple(('apple', 'banana', 'cherry'))
print(thisTuple)
